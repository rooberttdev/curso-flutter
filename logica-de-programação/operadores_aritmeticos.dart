void main (){


  //soma (+)
  
  final resultadoSoma = 10 + 5;

  print("Soma: $resultadoSoma");

  //subtração (-)

  final resultadoSubtracao = 10 - 26;

  print("Subtraçao: $resultadoSubtracao");

  //multiplicação (*)
  
  final resultadoMultiplicacao = 10 * 5;

  print("Multiplicação $resultadoMultiplicacao");

  //divisão (/)

  final resultadoDivisao = 10 / 5;

  print("Divisão: $resultadoDivisao");

  //resto da divisao (%)

  final restoDivisao = 26 % 4;

  print("Resto da divisão: $restoDivisao");




}